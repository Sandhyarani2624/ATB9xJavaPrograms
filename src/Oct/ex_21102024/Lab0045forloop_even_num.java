package Oct.ex_21102024;

public class Lab0045forloop_even_num {
    public static void main(String[] args) {
        for(int i = 0; i<=50; i++){
            if(i%2 == 0){
                System.out.println("Even");
            }
            else
            {
                System.out.println("Odd");
            }
        }
    }
}

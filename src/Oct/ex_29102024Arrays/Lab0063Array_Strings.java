package Oct.ex_29102024Arrays;

public class Lab0063Array_Strings {
    public static void main(String[] args) {
        String[] names = {"Sandhya", "Naresh", "Dharshini", "Gowtham" };
        System.out.println(names[0]);
        System.out.println(names[1]);
        System.out.println(names[2]);
        System.out.println(names[3]);

        boolean[] is_male_data = new boolean[2];
        is_male_data[0] = true;
        is_male_data[1] = false;

        System.out.println(is_male_data[0]);
        System.out.println(is_male_data[1]);
    }
}
